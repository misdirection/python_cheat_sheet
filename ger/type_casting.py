# String zu Integer
zahl_str = "123"
zahl_int = int(zahl_str)
print(zahl_int)  # Ausgabe: 123
print(type(zahl_int))  # Ausgabe: <class 'int'>

# String zu Float
kommazahl_str = "3.14"
kommazahl_float = float(kommazahl_str)
print(kommazahl_float)  # Ausgabe: 3.14
print(type(kommazahl_float))  # Ausgabe: <class 'float'>

# Integer zu String
zahl = 456
zahl_str = str(zahl)
print(zahl_str)  # Ausgabe: "456"
print(type(zahl_str))  # Ausgabe: <class 'str'>

# Float zu Integer
kommazahl = 7.89
ganzzahl = int(kommazahl)
print(ganzzahl)  # Ausgabe: 7
print(type(ganzzahl))  # Ausgabe: <class 'int'>

# Integer zu Float
ganzzahl = 10
kommazahl = float(ganzzahl)
print(kommazahl)  # Ausgabe: 10.0
print(type(kommazahl))  # Ausgabe: <class 'float'>
